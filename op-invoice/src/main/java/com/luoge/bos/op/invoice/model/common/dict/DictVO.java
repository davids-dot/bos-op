package com.luoge.bos.op.invoice.model.common.dict;

public class DictVO {

    private String code;

    private String value;

    public String getCode() {
        return code;
    }

    public DictVO setCode(String code) {
        this.code = code;
        return this;
    }

    public String getValue() {
        return value;
    }

    public DictVO setValue(String value) {
        this.value = value;
        return this;
    }
}
